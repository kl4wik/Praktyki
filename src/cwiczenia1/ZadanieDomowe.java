package cwiczenia1;

public class ZadanieDomowe {
    public static void main(String[] args) {
        // Wypisywanie liczb parzystych w przedziale od 1 do 10
        for (int i = 1; i <= 10; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
